package cart;

public class Ucart {
	protected int tid;
	protected int uid;
	protected int prodid;
	
	protected int qty;
	
	public Ucart(int tid, int uid, int prodid, int qty) {
		super();
		this.tid = tid;
		this.uid = uid;
		this.prodid = prodid;
		this.qty = qty;
	}
	public Ucart(int uid, int prodid, int qty) {
		super();
		this.uid = uid;
		this.prodid = prodid;
		this.qty = qty;
	}
	public Ucart(int uid) {
		super();
		this.uid = uid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getProdid() {
		return prodid;
	}
	public void setProdid(int prodid) {
		this.prodid = prodid;
	}
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	
	

}
